package com.friya.wurmonline.server.copypaste;

final class Pointf
{
    double x; 
    double y;

    public Pointf(double _x, double _y) {
        this.x = _x;
        this.y = _y;
    }   

    public double getX() {
        return this.x;
    }   

    public void setX(double _x) {
        this.x = _x;
    }   

    public double getY() {
        return this.y;
    }   

    public void setY(double _y) {
        this.y = _y;
    }   

}

